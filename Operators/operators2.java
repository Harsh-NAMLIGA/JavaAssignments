public class operators2 {

    public static void main(String[] args)
    {
            int n=5;
            System.out.println("pre increment valus of n is " + (++n));
        System.out.println("After ++n valus of n is " + n);
        System.out.println("post increment valus of n is " + (n++));
        System.out.println("After n++ valus of n is " + n);
        System.out.println("pre Decrement valus of n is " + (--n));
        System.out.println("After --n valus of n is " + n);
        System.out.println("post Decrement valus of n is " + (n--));
        System.out.println("After n-- valus of n is " + n);

    }
}
